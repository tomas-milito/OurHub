export function getWing(wingId) {
  return libraryWings.find((wing) => wing.id === wingId);
}

export function getBook(bookId) {
  for (const wing of libraryWings) {
    for (const section of wing.sections) {
      const book = section.books.find((item) => item.id === bookId);
      if (book) {
        return { book, section, wing };
      }
    }
  }

  return {};
}
